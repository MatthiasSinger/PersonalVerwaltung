package main.java.app.mitarbeiter.xml;

public class XMLAttribut {
	private final String name;
    private final String value;

	public XMLAttribut(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
}
